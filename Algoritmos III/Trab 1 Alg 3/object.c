#include "object.h"

/*----------------------------------------------------------------------------*/
/* Imprime um object em stream                                                */
/*----------------------------------------------------------------------------*/
void object_fprintf(FILE *stream, object o) {
	fprintf(stream, "%d", o);
}

/*----------------------------------------------------------------------------*/
/* Transforma um ASCII em um object                                           */
/*----------------------------------------------------------------------------*/
object object_atoo(char *s) {
	return atoi(s);
} 

/*----------------------------------------------------------------------------*/
/* Faz a compraracao entre dois objects                                       */
/*----------------------------------------------------------------------------*/
int object_cmp(object *o1, object *o2) {
	return *o1 < *o2 ? -1 : *o1 > *o2 ? 1 : 0;
}

/*----------------------------------------------------------------------------*/
/* Transforma um ASCII em um Hu3                                           */
/*----------------------------------------------------------------------------*/
object Hu3zodia(char *s) {
	return object_atoo(s);
}
